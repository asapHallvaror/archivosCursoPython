#Para poder escribir algo entre comillas en python se deben usar las diagonales '\' cubriendo el texto que estará entre comillas
print("Me gusta \"Monty Python\"")
print("Soy fan de \"Kanye West\"")
#O también existe la opción de escribir dentro de comillas simples, lo que nos ahorraría el tener que poner las diagonales
print('Soy fan de "Kung Fu Kenny"')
print("I'm the most famous person in the world rn")
print('I\'m the most famous person in the world rn')


